package Admistrar;

public class ControlConexion {
    private String Red;
    private int Megas;
    private String Compañia;

    public int getMegas() {
        return Megas;
    }
    public void setMegas(int megas) {
        Megas = megas;
    }
    public String getCompañia() {
        return Compañia;
    }
    public void setCompañia(String compañia) {
        Compañia = compañia;
    }
    public String getRed() {
        return Red;
    }
    public void setRed(String red) {
        Red = red;
    }
}
